package com.labs.lab1;

public class Monitor {
    String color;
    float height;
    float width;
    int resolutionX;
    int resolutionY;

    public Monitor(String color, float height, float width, int resolutionX, int resolutionY) {
        this.color = color;
        this.height = height;
        this.width = width;
        this.resolutionX = resolutionX;
        this.resolutionY = resolutionY;
    }
}
